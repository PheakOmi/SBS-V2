package com.ModelClasses;

public class Reset_Password{
	private String new_pw;
	private String confirm_pw;
	private int id;
	public String getNew_pw() {
		return new_pw;
	}
	public void setNew_pw(String new_pw) {
		this.new_pw = new_pw;
	}
	public String getConfirm_pw() {
		return confirm_pw;
	}
	public void setConfirm_pw(String confirm_pw) {
		this.confirm_pw = confirm_pw;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
}
